    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $data = $request->all();

        $new_file = new File();

        /* Inserisco file nella cartella storage */

        $file = Storage::put('files', $data['file']);

        $data['file'] = $file;

        $new_file->customer_id = $data['customer_id'];
        $new_file->file = $data['file'];

        $new_file->save();

        return redirect()->route('admin.customers.show', $data['customer_id'])->with('message', "File Caricato");
    }

